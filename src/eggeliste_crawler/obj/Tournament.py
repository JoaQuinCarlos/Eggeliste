class Tournament:

    def __init__(self, url, type, title, host, boards, rounds, pairs, year, month, date, pair_stats):
        self.url = url
        self.type = type
        self.title = title
        self.host = host
        self.boards = boards
        self.rounds = rounds
        self.pairs = pairs
        self.year = year
        self.month = month
        self.date = date
        self.pair_stats = pair_stats

